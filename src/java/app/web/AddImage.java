@MultipartConfig
public class AddImage extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        //processRequest(request, response);
        Part file = request.getPart("image");
        String foodName = request.getParameter("foodName");
        String foodPrice = request.getParameter("foodPrice");
        String imageFileName = file.getSubmittedFileName();
        String uploadPath = "C:\\Users\\DELL\\Documents\\NetBeansProjects\\admin_RAD\\web\\assetsadmin\\img\\food\\" + imageFileName;
        Connection con = DBConnector.getConnection();

        try {
            FileOutputStream fos = new FileOutputStream(uploadPath);
            InputStream is = file.getInputStream();

            byte[] data = new byte[is.available()];
            is.read(data);
            fos.write(data);
            fos.close();

        } catch (IOException e) {
            e.getMessage();
        }

        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO Food (Name, Price, Image) VALUES(?, ?, ?);");
            pst.setString(1, foodName);
            pst.setString(2, foodPrice);
            pst.setString(3, imageFileName);
            int a = pst.executeUpdate();

            if (a > 0) {
                response.sendRedirect("owner.jsp");
            } else {
                response.sendRedirect("owner.jsp");
            }
        } catch (SQLException e) {
            e.getMessage();
        }
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
